// Apply theme on page load
function applyStoredTheme() {
    const settings = JSON.parse(localStorage.getItem('userSettings')) || {};
    const theme = settings.theme || 'light';
    document.body.setAttribute('data-theme', theme);
}

applyStoredTheme();
